    private void btnAggActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAggActionPerformed

    if (!validarCampos()) {
        return;
    }
    
    try {
        ctrl.agregar(txtModel, txtCapa, comboAerolinea);
        ctrl.mostrarAviones(jTable1);
        limpiarCampos();
        txtModel.requestFocus();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al agregar avión: " + e.getMessage());
    }
    }//GEN-LAST:event_btnAggActionPerformed

