    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
    int filaSeleccionada = jTable1.getSelectedRow();
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Debe seleccionar un avión para eliminar");
        return;
    }
    int confirmacion = JOptionPane.showConfirmDialog(
        this, 
        "¿Está seguro de eliminar este avión?", 
        "Confirmar eliminación", 
        JOptionPane.YES_NO_OPTION
    );
    
    if (confirmacion == JOptionPane.YES_OPTION) {
        try {
            int idAvion = (int) jTable1.getValueAt(filaSeleccionada, 0);
            ctrl.eliminar(idAvion);
            ctrl.mostrarAviones(jTable1);
            limpiarCampos();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar avión: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnDelActionPerformed

    private void btnEdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdActionPerformed
    int filaSeleccionada = jTable1.getSelectedRow();
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Debe seleccionar un avión para editar");
        return;
    }
    if (!validarCampos()) {
        return;
    }
    
    try {
        int idAvion = (int) jTable1.getValueAt(filaSeleccionada, 0);
        ctrl.actualizar(idAvion, txtModel, txtCapa, comboAerolinea);
        ctrl.mostrarAviones(jTable1);
        limpiarCampos();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al actualizar avión: " + e.getMessage());
    }
    }//GEN-LAST:event_btnEdActionPerformed

